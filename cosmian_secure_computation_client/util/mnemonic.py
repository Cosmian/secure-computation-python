def random_words(lang: Literal["english", "french"] = "english") -> (str, str, str):
    if lang == "english":
        return (secrets.choice(ENGLISH),
                secrets.choice(ENGLISH),
                secrets.choice(ENGLISH))

    if lang == "french":
        return (secrets.choice(FRENCH),
                secrets.choice(FRENCH),
                secrets.choice(FRENCH))

    raise Exception("unsupported language")


def check_words(words: Tuple[str, str, str]) -> bool:
    english_bip39: List[bytes] = [normalize("NFKD", word).encode("ascii", "ignore")
                                  for word in ENGLISH]
    french_bip39: List[bytes] = [normalize("NFKD", word).encode("ascii", "ignore")
                                 for word in FRENCH]

    for word in words:
        w: bytes = normalize("NFKD", word).encode("ascii", "ignore")

        if w not in english_bip39 or w not in french_bip39:
            return False

    return True
